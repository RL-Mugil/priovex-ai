import type { PatentReport } from '@priovex/types';
import { format } from 'date-fns';

export function generateMarkdownReport(report: PatentReport): string {
  const date = format(new Date(report.generatedAt), 'MMMM d, yyyy');
  const { patentabilityAssessment: pa, claimStrategy: cs, statistics: st } = report;

  const verdictEmoji: Record<string, string> = {
    PROCEED: '✅',
    PROCEED_WITH_CAUTION: '⚠️',
    REFINE_FIRST: '🔄',
    UNLIKELY: '❌',
  };

  const ratingEmoji: Record<string, string> = {
    HIGH: '🟢',
    'MEDIUM-HIGH': '🟡',
    MEDIUM: '🟡',
    'MEDIUM-LOW': '🟠',
    LOW: '🔴',
  };

  const lines: string[] = [
    `# PRIOR ART SEARCH REPORT`,
    `## ${report.inventionTitle}`,
    ``,
    `---`,
    ``,
    `**Generated by:** PrioVex.AI  `,
    `**Date:** ${date}  `,
    `**AI Provider:** ${st.aiProvider.toUpperCase()}  `,
    `**Report Style:** ${report.reportStyle.charAt(0).toUpperCase() + report.reportStyle.slice(1)}  `,
    `**Patents Reviewed:** ${st.totalPatentsReviewed.toLocaleString()}  `,
    `**Search Duration:** ${Math.round(st.searchDurationSeconds / 60)} minutes  `,
    ``,
    `---`,
    ``,
    `## Executive Summary`,
    ``,
    report.executiveSummary,
    ``,
    `---`,
    ``,
    `## Patentability Assessment`,
    ``,
    `### Overall Verdict: ${verdictEmoji[pa.overallVerdict] ?? ''} ${pa.overallVerdict.replace(/_/g, ' ')}`,
    ``,
    `| Criterion | Rating | Score |`,
    `|-----------|--------|-------|`,
    `| **Novelty (35 USC 102)** | ${ratingEmoji[pa.noveltyRating] ?? ''} ${pa.noveltyRating} | ${pa.patentabilityScore}/100 |`,
    `| **Non-Obviousness (35 USC 103)** | ${ratingEmoji[pa.obviousnessRating] ?? ''} ${pa.obviousnessRating} | — |`,
    ``,
    `### Novelty Analysis`,
    ``,
    pa.noveltyAnalysis,
    ``,
    `### Non-Obviousness Analysis`,
    ``,
    pa.obviousnessAnalysis,
    ``,
    `### Key Risks`,
    ``,
    ...pa.keyRisks.map((r) => `- ${r}`),
    ``,
    `### Key Opportunities`,
    ``,
    ...pa.keyOpportunities.map((o) => `- ${o}`),
    ``,
    `### White Space Areas`,
    ``,
    ...pa.whiteSpaceAreas.map((w) => `- ${w}`),
    ``,
    `---`,
    ``,
    `## Top 5 Most Relevant Prior Art`,
    ``,
    ...report.topPriorArt.slice(0, 5).flatMap((patent, i) => [
      `### ${i + 1}. ${patent.publicationNumber} — ${patent.title}`,
      ``,
      `**Relevance Score:** ${patent.relevanceScore}% | **Similarity:** ${patent.similarityScore}% | **Impact:** ${patent.noveltyImpact.toUpperCase()}  `,
      `**Assignee:** ${patent.assignees[0] ?? 'N/A'}  `,
      `**Filed:** ${patent.filingDate} | **CPC:** ${patent.cpcCodes.slice(0, 3).join(', ')}  `,
      `**URL:** [${patent.publicationNumber}](${patent.url})`,
      ``,
      `**Abstract:** ${patent.abstract.slice(0, 400)}${patent.abstract.length > 400 ? '...' : ''}`,
      ``,
      `**Similarities:**`,
      ...patent.similarities.map((s) => `- ${s}`),
      ``,
      `**Key Differences:**`,
      ...patent.differences.map((d) => `- ${d}`),
      ``,
      `**Analysis:** ${patent.analysis}`,
      ``,
      `---`,
      ``,
    ]),
    ``,
    `## Claim Strategy`,
    ``,
    `### Recommended Independent Claim`,
    ``,
    `\`\`\``,
    cs.independentClaimSuggestion,
    `\`\`\``,
    ``,
    `### Dependent Claims`,
    ``,
    ...cs.dependentClaimSuggestions.map((c, i) => `${i + 1}. ${c}`),
    ``,
    `### Claiming Approach`,
    ``,
    cs.claimingApproach,
    ``,
    `### Prosecution Strategy`,
    ``,
    cs.prosecutionStrategy,
    ``,
    `### Elements to Emphasize`,
    ``,
    ...cs.elementsToEmphasize.map((e) => `- ✅ ${e}`),
    ``,
    `---`,
    ``,
    `## IDS References (USPTO Information Disclosure Statement)`,
    ``,
    `The following references must be disclosed to the USPTO:`,
    ``,
    ...report.idsReferences.map((ref, i) => `${i + 1}. ${ref}`),
    ``,
    `---`,
    ``,
    `## Search Statistics`,
    ``,
    `| Metric | Value |`,
    `|--------|-------|`,
    `| Total Patents Reviewed | ${st.totalPatentsReviewed.toLocaleString()} |`,
    `| Relevant Patents Found | ${st.relevantPatentsFound.toLocaleString()} |`,
    `| Top Prior Art Selected | ${st.topPriorArtSelected} |`,
    `| NPL References Found | ${st.nplReferencesFound ?? 0} |`,
    `| NPL References Analyzed | ${st.nplReferencesAnalyzed ?? 0} |`,
    `| Novel Elements Decomposed | ${st.novelElementsDecomposed ?? 0} |`,
    `| Coverage Matrix | ${st.coverageMatrixSize ?? 'N/A'} |`,
    `| IDS Entries Generated | ${st.idsEntriesGenerated ?? 0} |`,
    `| BigQuery Data Processed | ${(Number(st.bigQueryBytesProcessed) / 1e9).toFixed(2)} GB |`,
    `| AI Tokens Used | ${st.aiTokensUsed.toLocaleString()} |`,
    `| Search Duration | ${Math.round(st.searchDurationSeconds / 60)} minutes |`,
    ``,
    `### Keywords Searched`,
    ``,
    st.keywordsSearched.map((k) => `\`${k}\``).join(' · '),
    ``,
    `### CPC Codes Analyzed`,
    ``,
    st.cpcCodesSearched.map((c) => `\`${c}\``).join(' · '),
    ``,
    ...(st.nplSourcesSearched?.length ? [
      `### NPL Sources Searched`,
      ``,
      st.nplSourcesSearched.map((s) => `- ${s}`).join('\n'),
      ``,
    ] : []),
    `---`,
    ``,

    // ── v2: Novel Elements ──────────────────────────────────────────────────
    ...(report.novelElements?.length ? [
      `## Novel Element Decomposition`,
      ``,
      `The following ${report.novelElements.length} claim-like novel elements were extracted:`,
      ``,
      ...report.novelElements.map((e) =>
        `**[${e.label}]** (Novelty weight: ${e.noveltyWeight}%)  \n${e.claimLanguage}`
      ).flatMap((s) => [s, '']),
      `---`,
      ``,
    ] : []),

    // ── v2: NPL References ─────────────────────────────────────────────────
    ...(report.nplReferences?.length ? [
      `## Non-Patent Literature (NPL) References`,
      ``,
      ...report.nplReferences.slice(0, 10).flatMap((n, i) => [
        `### NPL ${i + 1}: ${n.title}`,
        ``,
        `**Source:** ${n.source} | **Relevance:** ${n.relevanceScore}% | **Impact:** ${n.noveltyImpact?.toUpperCase() ?? 'PENDING'}  `,
        `**Authors:** ${n.authors.join(', ') || 'N/A'}  `,
        `**Date:** ${n.publicationDate} | **URL:** [${n.url}](${n.url})`,
        ``,
        `**Abstract:** ${n.abstract.slice(0, 400)}${n.abstract.length > 400 ? '...' : ''}`,
        ``,
        ...(n.similarities?.length ? [`**Similarities:** ${n.similarities.slice(0, 2).join('; ')}`] : []),
        ``,
        `---`,
        ``,
      ]),
    ] : []),

    // ── v2: Coverage Matrix ────────────────────────────────────────────────
    ...(report.coverageMatrix?.elements?.length && report.coverageMatrix?.references?.length ? [
      `## Feature-by-Feature Coverage Matrix`,
      ``,
      `| Novel Element | ${report.coverageMatrix.references.map((r) => r.number).join(' | ')} |`,
      `|${'-'.repeat(40)}|${report.coverageMatrix.references.map(() => '------').join('|')}|`,
      ...report.coverageMatrix.elements.map((e) => {
        const cells = report.coverageMatrix.cells[e.id] ?? {};
        const stateMap: Record<string, string> = {
          fully_covered: '✓', partially_covered: '~', implied: '≈',
          not_covered: '✗', ambiguous: '?',
        };
        const row = report.coverageMatrix.references.map((r) => {
          const cell = cells[r.id];
          return cell ? stateMap[cell.state] ?? '?' : '—';
        }).join(' | ');
        return `| ${e.claimLanguage.slice(0, 50)}... | ${row} |`;
      }),
      ``,
      `**Legend:** ✓ Fully Covered | ~ Partially | ≈ Implied | ✗ Not Covered | ? Ambiguous`,
      ``,
      `---`,
      ``,
    ] : []),

    // ── v2: Examiner Simulation ────────────────────────────────────────────
    ...(report.examinerPrediction?.firstOfficeActionScenario ? [
      `## Examiner Simulation`,
      ``,
      `### Most Likely First Office Action`,
      ``,
      `**Rejection Basis:** ${report.examinerPrediction.firstOfficeActionScenario.rejectionBasis}`,
      ``,
      `**Predicted References:** ${report.examinerPrediction.firstOfficeActionScenario.predictedReferences.join(', ')}`,
      ``,
      `**Recommended Response:** ${report.examinerPrediction.firstOfficeActionScenario.responseStrategy}`,
      ``,
      `**Estimated Allowance Probability:** ${report.examinerPrediction.firstOfficeActionScenario.estimatedAllowanceChance}%`,
      ``,
      ...(report.examinerPrediction.section112Risks?.length ? [
        `**35 USC 112 Risks:**`,
        ...report.examinerPrediction.section112Risks.map((r) => `- ${r}`),
        ``,
      ] : []),
      `---`,
      ``,
    ] : []),

    // ── Client Summary ─────────────────────────────────────────────────────
    ...(report.clientSummary ? (() => {
      const cs2 = report.clientSummary!;
      const verdictIcon = cs2.isPatentable ? '✅' : '❌';
      const confidenceLabel = cs2.confidence === 'high' ? 'High Confidence' : cs2.confidence === 'medium' ? 'Medium Confidence' : 'Low Confidence';
      return [
        `## Summary for You (Plain Language)`,
        ``,
        `> This section is written for you as the inventor — no legal jargon.`,
        ``,
        `### ${verdictIcon} ${cs2.plainVerdict}`,
        ``,
        `**Confidence:** ${confidenceLabel}`,
        ``,
        cs2.reason,
        ``,
        `**Main Risk:** ${cs2.mainRisk}`,
        ``,
        `**What to do next:** ${cs2.nextStep}`,
        ``,
        `---`,
        ``,
      ];
    })() : []),

    // ── Legal Disclaimer ───────────────────────────────────────────────────
    `## Legal Disclaimer`,
    ``,
    `*This report was prepared by PrioVex.AI — AI-Assisted Patent Intelligence Platform.*  `,
    `*It does not constitute legal advice. Consult a registered patent attorney or agent before filing.*  `,
    `*Prior art searches are not exhaustive. AI-assisted analysis requires professional review.*`,
    ``,
    `*Report generated by PrioVex.AI v2 | mugilvannan@myipstrategy.com*`,
  ];

  return lines.join('\n');
}
